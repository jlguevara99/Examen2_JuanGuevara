    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        try {
            String nombre = "", id = "", rango = "", arma = "";
            int poder = 0;
            if (ak.isSelected()) {
                arma = "AK 47";
                poder = 27;
            } else if (rev.isSelected()) {
                arma = "Revolver";
                poder = 13;
            } else {
                arma = "RPG-7";
                poder = 57;
            }
            nombre = ru_nombre.getText();
            id = ru_id.getText();
            rango = ru_rango.getText();
            int edad = Integer.parseInt(ru_edad.getText());
            int resistencia = edad * 8;
            if (edad < 25 && poder == 57) {
                JOptionPane.showMessageDialog(this, "No puede usar un rpg");
            } else if (edad < 18) {
                JOptionPane.showMessageDialog(this, "No tiene suficiente edad");
            } else {
                rusos.add(new Ruso(edad, id, nombre, resistencia, arma, rango, poder));
                Binario b = new Binario("./Datos.jlg");

                //Cargar al archivo
                b.cargarArchivo();
                b.setcosas(new Ruso(edad, id, nombre, resistencia, arma, rango, poder));
                b.escribirArchivo();

                JOptionPane.showMessageDialog(this, "Soldado Ruso Agregado");
                ru_nombre.setText("");
                ru_id.setText("");
                ru_rango.setText("");
                ru_edad.setText("");
                ak.setSelected(true);
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton3MouseClicked

